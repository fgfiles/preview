HH.Format = function (value) {
    var s = value + '';
    var str = '';
    var c = -1;
    for(var i = 0; i<s.length; i++) {
        c++;
        if(c > 2) {
            str = ',' + str;
            c=0;
        }
        str = [s.charAt(s.length - 1 - i)] + str;
    }
    return str;
};


